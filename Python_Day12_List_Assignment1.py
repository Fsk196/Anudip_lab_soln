#1.Write a Python program to sum all the items in a list.

# Function to sum all the items in a list
def sum_of_list(items):
    total = sum(items)  # Use the built-in sum() function to calculate the sum
    return total

# Example list
my_list = [3, 5, 6, 7, 8]

# Calculate the sum
result = sum_of_list(my_list)

# Print the result
print("The sum of all the items in the list is:", result)

#Ans:The sum of all the items in the list is: 29


