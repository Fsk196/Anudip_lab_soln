"""2. Declare a square() function with one parameter.Then call the function
and pass one number and display the square of that number"""

def square(number):
    # Calculate the square of the number
    result = number ** 2
    print(f"The square of {number} is: {result}")

# Call the function with one number
square(9)  # Example: The square of 9

#Ans: The square of 9 is: 81

